    private void nomActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_nomActionPerformed
    {//GEN-HEADEREND:event_nomActionPerformed
        nomrep = Manipulacion.verifNR(arre, nom.getText());
        if (nomrep == true) //si se repite entonces manda un mensaje
        {
            Mensajes.error(this, "Nombre repetido. Ingrese un nombre diferente");
            CtrlInterfaz.limpia(nom);
            CtrlInterfaz.habilita(false, nom);
            CtrlInterfaz.cambia(nom);
        }
    }//GEN-LAST:event_nomActionPerformed

