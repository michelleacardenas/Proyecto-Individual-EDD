    private void aceptarBActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarBActionPerformed
    {//GEN-HEADEREND:event_aceptarBActionPerformed
        try
        {

            if (Integer.valueOf(priori.getText()) == 0 || Integer.valueOf(quant.getText()) == 0)
            {
                Mensajes.error(this, "Los valores no pueden ser igual a 0");
                CtrlInterfaz.limpia(priori, quant, nom);
                CtrlInterfaz.cambia(priori);
            } else
            {
                Proceso proc = new Proceso(Integer.valueOf(priori.getText()), nom.getText(), Integer.valueOf(quant.getText()));
                lc = Manipulacion.inserta(proc, lc);
                arre = Manipulacion.insertaNom(arre, nom.getText());

                Archivos.guarda(this, arre, "Arre.dat");
                Archivos.guarda(this, lc, "Datos.dat");

                Mensajes.exito(this, "Datos guardados con √©xito");
                CtrlInterfaz.limpia(priori, nom, quant);
                CtrlInterfaz.habilita(false, nom, quant, aceptarB);
                CtrlInterfaz.cambia(priori);
            }

        } catch (Exception e)
        {
            Mensajes.error(this, "Error: " + e.toString());
        }
    }//GEN-LAST:event_aceptarBActionPerformed

