    private void prioriActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_prioriActionPerformed
    {//GEN-HEADEREND:event_prioriActionPerformed
        try
        {
            if (VtnAltas.lc != null)
            {
                if (priori.getText().equals(""))
                {
                    Mensajes.error(this, "No se puede buscar un campo vacio ");
                    CtrlInterfaz.limpia(priori);
                    CtrlInterfaz.habilita(false, priori);
                    CtrlInterfaz.cambia(priori);
                } else
                {
                    existePr = Manipulacion.verifEP(VtnAltas.lc, Integer.parseInt(priori.getText()));

                    if (existePr == false)
                    {
                        Mensajes.error(this, "No existe la prioridad. Ingrese una prioridad diferente");
                        CtrlInterfaz.limpia(priori);
                        CtrlInterfaz.habilita(false, priori);
                        CtrlInterfaz.cambia(priori);
                    } else
                    {
                        Manipulacion.consultasTab(VtnAltas.lc, jTableCons, Integer.valueOf(priori.getText()));
                    }
                }

            } else
            {
                Mensajes.error(this, "No existen procesos registrados, ingresa primero datos");
                this.setVisible(false);
                new VtnAltas().setVisible(true);
            }

        } catch (Exception e)
        {
            Mensajes.error(this, "Error: " + e.toString());
        }


    }//GEN-LAST:event_prioriActionPerformed

